
public class loop_exercise2 {
	public static void main (String[] args) {
		
		int i = 10;
		while (i > 0) {
			System.out.println(i);
			i--;
		}
		
		do_loop();	
		for_loop2();}
	
		
	
	public static void do_loop() {
		System.out.println("\n");
		int x = 10;

		do {
			System.out.println(x);
			x--;
		
		}
		while(x > 0);
		System.out.println("\n");
		
	}	

	
	
	public static void for_loop2() {
		
		for(int y = 10 ; y > 0 ; y -- )
			System.out.println(y);
		
	}
	
	
	
}


		
		
		
		
		
	

